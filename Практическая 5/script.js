// Задача 1: Калькулятор (стрелочная функция)
const посчитать = () => {
    // Получаем значения из полей ввода
    let число1 = Number(document.getElementById('первоеЧисло').value);
    let число2 = Number(document.getElementById('второеЧисло').value);
    let действие = document.getElementById('действие').value;
    
    let ответ;
    
    // Выполняем вычисления
    if (действие === '+') {
        ответ = число1 + число2;
    }
    if (действие === '-') {
        ответ = число1 - число2;
    }
    if (действие === '*') {
        ответ = число1 * число2;
    }
    if (действие === '/') {
        if (число2 === 0) {
            ответ = 'На ноль делить нельзя!';
        } else {
            ответ = число1 / число2;
        }
    }
    
    // Показываем результат
    document.getElementById('результатКалькулятора').textContent = 
        `Получилось: ${ответ}`;
};

// Задача 2: Проверка на палиндром (стрелочная функция)
const проверитьПалиндром = () => {
    // Получаем фамилию
    let фамилия = document.getElementById('фамилия').value;
    
    // Делаем буквы маленькими и убираем пробелы
    фамилия = фамилия.toLowerCase().replace(/ /g, '');
    
    // Переворачиваем фамилию
    let перевёрнутаяФамилия = '';
    for(let i = фамилия.length - 1; i >= 0; i--) {
        перевёрнутаяФамилия += фамилия[i];
    }
    
    // Проверяем, палиндром или нет
    let ответ = '';
    if (фамилия === перевёрнутаяФамилия) {
        ответ = 'Да, это палиндром!';
    } else {
        ответ = 'Нет, это не палиндром';
    }
    
    document.getElementById('результатПалиндрома').textContent = ответ;
};

// Задача 3: Среднее значение массива (стрелочная функция)
const числаМассива = [2, 4, 6, 8, 10];
const среднееЗначение = (массив) => {
    let сумма = 0;
    for(let i = 0; i < массив.length; i++) {
        сумма += массив[i];
    }
    return сумма / массив.length;
};

// Задача 4: Дни до Нового года (стрелочная функция)
const днейДоНовогоГода = () => {
    let сегодня = new Date();
    let новыйГод = new Date(сегодня.getFullYear() + 1, 0, 1);
    let разницаВМиллисекундах = новыйГод - сегодня;
    return Math.ceil(разницаВМиллисекундах / (1000 * 60 * 60 * 24));
};

